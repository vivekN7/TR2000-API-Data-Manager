CREATE INDEX IDX_PLANTS_ETL ON TR2KSTG_PLANTS(ETL_RUN_ID);
CREATE INDEX IDX_ISSUES_ETL ON TR2KSTG_ISSUES(ETL_RUN_ID);

-- Indexes on foreign key relationships
