CREATE INDEX IDX_PLANTS_CURRENT ON TR2KSTG_PLANTS(IS_CURRENT);
CREATE INDEX IDX_ISSUES_CURRENT ON TR2KSTG_ISSUES(IS_CURRENT);
CREATE INDEX IDX_PCS_REF_CURRENT ON TR2KSTG_PCS_REFERENCES(IS_CURRENT);

-- Indexes on ETL_RUN_ID for joins
